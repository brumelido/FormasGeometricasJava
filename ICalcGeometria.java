package Atividade041122;

public class ICalcGeometria {
    private int base;
    private int altura;
    private int perimetro;
    private int area;
    private int raiodocirculo;

    public int getaltura() {
        return altura;
    }
    public int getBase() {
        return base;
    }
    public int getPerimetro() {
        return perimetro;
    }
    public int getArea() {
        return area;
    }
    public int getRaiodocirculo() {
        return raiodocirculo;
    }
    public void setaltura(int altura) {
        this.altura = altura;
    }
    public void setBase(int base) {
        this.base = base;
    }
    public void setPerimetro(int perimetro) {
        this.perimetro = perimetro;
    }
    public void setArea(int area) {
        this.area = area;
    }
    public void setRaiodocirculo(int raiodocirculo) {
        this.raiodocirculo = raiodocirculo;
    }
}
